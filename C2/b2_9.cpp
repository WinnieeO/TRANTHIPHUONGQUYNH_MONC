// BT9: Cyborg Data Type Sizes
#include <iostream>
using namespace std;

int main()
{
    // Xuat luong bo nho cua cac kieu du lieu char, int, float, double
    cout << "The size of a char is " << sizeof(char) << " byte.\n";
    cout << "The size of an integer is " << sizeof(int) << " bytes.\n";
    cout << "The size of a float is " << sizeof(float) << " bytes.\n";
    cout << "The size of a double is " << sizeof(double) << " bytes.\n";
    return 0;
}